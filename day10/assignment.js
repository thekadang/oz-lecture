
// 대입 연산자
let x = 10;

// 복합 대입연산자
x += 10;  // x = x+10
console.log(x); //20   += : x 값에 x+10

let y = 10;
y -= 5; // y = y-5
console.log(y); // 5   

let z = 10;
z *= 5 // z = z*5
console.log(z); // 50

let u = 10;
u /= 5; // u = u / 5
console.log(u); //2

let v = 10
v %= 3;
console.log(v); //1

let t = 10;
t **= 3;
console.log(t); // 1000


