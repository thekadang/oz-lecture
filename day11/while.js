// 1부터 최대 100까지 1씩 증가하는 while 반복문
let num = 1;
while(num <= 100) {
    console.log(num);
    num++;  // num = num +1;
}

console.log('반복문 종료');
console.log('num = ',num);

// 1부터 최대 100까지 2씩 증가하는 while반복문

